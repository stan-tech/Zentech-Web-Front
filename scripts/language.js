  // Function to change the language
function changeLanguage(lang) {
  // Update regular text content
  const elements = document.querySelectorAll("[data-key]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-key");
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  const placeholders = document.querySelectorAll("[data-placeholder-key]");
  placeholders.forEach((input) => {
    const key = input.getAttribute("data-placeholder-key");
    if (translations[lang][key]) {
      input.setAttribute("placeholder", translations[lang][key]);
    }
  });
}

window.onload = async () => {
  changeLanguage("en");

  const customHeader = document.querySelector('custom-header');

  if (customHeader) {
    const observer = new MutationObserver(() => {
      const languageSelector = customHeader.querySelector('#language-selector');
      if (languageSelector) {
        languageSelector.addEventListener('change', (event) => {
          const selectedLanguage = event.target.value;
          changeLanguage(selectedLanguage);
        });
        observer.disconnect(); // Stop observing once the element is found
      }
    });
  
    observer.observe(customHeader, { childList: true, subtree: true });
  }
};